const navToggle = document.querySelector('.nav-toggle');
const navLinks = document.getElementById('navLinks');
const themeToggle = document.getElementById('themeToggle');
const languageSelect = document.getElementById('languageSelect');
const modal = document.getElementById('enquiryModal');
const openButtons = document.querySelectorAll('.open-enquiry');
const closeButtons = document.querySelectorAll('[data-close-modal]');
const enquiryForm = document.getElementById('enquiryForm');

function setLanguage(lang) {
  if (!['en', 'ar'].includes(lang)) lang = 'en';
  const dict = translations[lang] || translations.en;
  const fallback = translations.en;
  document.documentElement.lang = lang;
  document.documentElement.dir = 'ltr';
  document.body.setAttribute('data-lang', lang);
  document.body.classList.toggle('lang-ar', lang === 'ar');
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const value = dict[key] || fallback[key] || '';
    if (value) el.textContent = value;
  });
  localStorage.setItem('dcl_lang', lang);
}

function setTheme(theme) {
  document.body.classList.toggle('dark', theme === 'dark');
  localStorage.setItem('dcl_theme', theme);
}

if (navToggle && navLinks) {
  navToggle.addEventListener('click', () => {
    const open = navLinks.classList.toggle('active');
    navToggle.setAttribute('aria-expanded', String(open));
  });
  navLinks.querySelectorAll('a').forEach(link => link.addEventListener('click', () => {
    navLinks.classList.remove('active');
    navToggle.setAttribute('aria-expanded', 'false');
  }));
}

if (themeToggle) {
  themeToggle.addEventListener('click', () => {
    setTheme(document.body.classList.contains('dark') ? 'light' : 'dark');
  });
}

if (languageSelect) {
  languageSelect.addEventListener('change', (e) => setLanguage(e.target.value));
}

function openModal() {
  modal.classList.add('active');
  modal.setAttribute('aria-hidden', 'false');
  document.body.style.overflow = 'hidden';
}
function closeModal() {
  modal.classList.remove('active');
  modal.setAttribute('aria-hidden', 'true');
  document.body.style.overflow = '';
}
openButtons.forEach(button => button.addEventListener('click', openModal));
closeButtons.forEach(button => button.addEventListener('click', closeModal));
document.addEventListener('keydown', e => {
  if (e.key === 'Escape' && modal.classList.contains('active')) closeModal();
});

if (enquiryForm) {
  enquiryForm.addEventListener('submit', event => {
    event.preventDefault();
    const data = new FormData(enquiryForm);
    const name = encodeURIComponent(data.get('name'));
    const contact = encodeURIComponent(data.get('contact'));
    const message = encodeURIComponent(data.get('message'));
    const subject = encodeURIComponent('Website Enquiry - Doha Call Line');
    const body = `Name:%20${name}%0AEmail%20/%20Phone:%20${contact}%0A%0AMessage:%0A${message}`;
    window.location.href = `mailto:info@dohacallline.com?subject=${subject}&body=${body}`;
  });
}

