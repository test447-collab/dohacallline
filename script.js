// Smooth reveal animation
const revealObserver = new IntersectionObserver(entries => {
  entries.forEach(entry => {
    if (entry.isIntersecting) {
      entry.target.classList.add('visible');
      revealObserver.unobserve(entry.target);
    }
  });
}, { threshold: 0.12, rootMargin: '0px 0px -40px 0px' });

document.querySelectorAll('.reveal').forEach(el => revealObserver.observe(el));

// Project category accordion
const projectTabs = document.querySelectorAll('.project-tab');
const projectPanels = document.querySelectorAll('.project-panel');
projectTabs.forEach(tab => {
  tab.addEventListener('click', () => {
    const key = tab.getAttribute('data-project');
    const panel = document.getElementById(`panel-${key}`);
    const alreadyActive = tab.classList.contains('active') && panel && panel.classList.contains('active');

    projectTabs.forEach(item => item.classList.remove('active'));
    projectPanels.forEach(item => item.classList.remove('active'));

    if (!alreadyActive && panel) {
      tab.classList.add('active');
      panel.classList.add('active');
    }
  });
});

