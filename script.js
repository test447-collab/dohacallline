// Major Clients image sliders: 3 images per company, clean automatic fade + clickable dots
const clientCards = document.querySelectorAll('.client-showcase');
clientCards.forEach((card, cardIndex) => {
  const slides = card.querySelectorAll('.client-slide');
  const dots = card.querySelectorAll('.client-dot');
  let index = 0;
  let timer;

  function showClientSlide(nextIndex) {
    index = nextIndex;
    slides.forEach((slide, i) => slide.classList.toggle('active', i === index));
    dots.forEach((dot, i) => dot.classList.toggle('active', i === index));
  }

  function startSlider() {
    timer = setInterval(() => {
      showClientSlide((index + 1) % slides.length);
    }, 3200 + (cardIndex * 180));
  }

  dots.forEach((dot, i) => {
    dot.addEventListener('click', () => {
      clearInterval(timer);
      showClientSlide(i);
      startSlider();
    });
  });

  startSlider();
});
